class Conversiones {
double MillaMetro(double m){

   double metros = m * 1609.34;

return metros;

}
double MillaDecimetros(double g){

    double decimetros = g * 16093.4;
    return decimetros;
}

double MillaCentimetros(double c){
    double centimetros = c * 160934;
    return centimetros;
}

double MillaKilometros(double e){

    double kilometros = e * 1.60934;
    return kilometros;
}
}
